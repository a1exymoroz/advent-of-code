  checkDirTail(head, tail) {
    let dirOut;

    const dirs = [
      {
        dir: "U",
        isEqual: head.y + 2 === tail.y,
        loop: [head.x - 1, head.x + 1],
        key: "x",
      },
      {
        dir: "R",
        isEqual: head.x - 2 === tail.x,
        loop: [head.y - 1, head.y + 1],
        key: "y",
      },
      {
        dir: "D",
        isEqual: head.y - 2 === tail.y,
        loop: [head.x - 1, head.x + 1],
        key: "x",
      },
      {
        dir: "L",
        isEqual: head.x + 2 === tail.x,
        loop: [head.y - 1, head.y + 1],
        key: "y",
      },
    ];

    dirs.forEach(({ dir, isEqual, loop, key }) => {
      const [start, end] = loop;

      for (let index = start; index <= end; index++) {
        if (isEqual && tail[key] === index) {
          dirOut = dir;
          return;
        }
      }
    });

    return dirOut;
  }

  getFreeLength(dir, value) {
    let number = 0;
    switch (dir) {
      case "U":
        const lengthU = this.head.y - +value;
        number = lengthU < 0 ? lengthU : 0;
        break;
      case "R":
        const lengthR = this.head.x + +value;
        number =
          lengthR > this.grid[0].length - 1
            ? lengthR - (this.grid[0].length - 1)
            : 0;
        break;
      case "D":
        const lengthD = this.head.y + +value;
        number =
          lengthD > this.grid.length - 1 ? lengthD - (this.grid.length - 1) : 0;
        break;
      case "L":
        const lengthL = this.head.x - +value;
        number = lengthL < 0 ? lengthL : 0;
        break;
    }

    return Math.abs(number);
  }

  fillGrid(dir, value) {
    switch (dir) {
      case "U":
        this.fillTop(value);
        return;
      case "R":
        this.fillRight(value);
        return;
      case "D":
        this.fillBottom(value);
        return;
      case "L":
        this.fillLeft(value);
        return;
    }
  }

  fillTop(length) {
    const newItems = Array.from({ length: length }, () =>
      Array.from({ length: this.grid[0].length }, () => "0")
    );

    this.grid = [...newItems, ...this.grid];
    this.snake.forEach((element) => {
      if (element) {
        element.y += length;
      }
    });
    this.offset.y += length;
  }

  fillRight(length) {
    const newItems = Array.from({ length: length }, () => "0");

    for (let index = 0; index < this.grid.length; index++) {
      this.grid[index] = [...this.grid[index], ...newItems];
    }
  }

  fillBottom(length) {
    const newItems = Array.from({ length: length }, () =>
      Array.from({ length: this.grid[0].length }, () => "0")
    );

    this.grid = [...this.grid, ...newItems];
  }

  fillLeft(length) {
    const newItems = Array.from({ length: length }, () => "0");

    for (let index = 0; index < this.grid.length; index++) {
      this.grid[index] = [...newItems, ...this.grid[index]];
    }

    this.snake.forEach((element) => {
      if (element) {
        element.x += length;
      }
    });
    this.offset.x += length;
  }
