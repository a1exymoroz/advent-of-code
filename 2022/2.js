// X for Rock, Y for Paper, and Z for Scissors
// 1 for Rock, 2 for Paper, and 3 for Scissors
// 0 if you lost, 3 if the round was a draw, and 6 if you won
// X means you need to lose, Y means you need to end the round in a draw, and Z means you need to win

const rockPaperScissors = (data) => {
  let sum = 0;

  const games = data.split("\n");
  const points = {
    X: 1,
    Y: 2,
    Z: 3,
    "A X": 3,
    "A Y": 6,
    "A Z": 0,
    "B X": 0,
    "B Y": 3,
    "B Z": 6,
    "C X": 6,
    "C Y": 0,
    "C Z": 3,
  };

  const switchCondition = {
    "A X": "A Z",
    "A Y": "A X",
    "A Z": "A Y",
    "B X": "B X",
    "B Y": "B Y",
    "B Z": "B Z",
    "C X": "C Y",
    "C Y": "C Z",
    "C Z": "C X",
  };

  games.forEach((element) => {
    if (!element) {
      return;
    }
    element = switchCondition[element];
    const [a, b] = element.split(" ");
    // console.log(element)
    sum += points[b] + points[element];
    // console.log(sum)
  });

  // console.log(games)

  return sum;
};

